void bootloader(void)
{
	bl = 1;
	while(1)
	{
		rt_bus_proto_process();
		rt_bus_proto_clear();
		//HAL_Delay(1);
	}
}

void bootloader_2(void)
{
	uint32_t cnt = 0;
	bl = 1;
	while(1)
	{
		rt_bus_proto_process();
		rt_bus_proto_clear();
		HAL_Delay(1);
		cnt++;

		if (cnt >= 1000)
		{
			dbg_printf("A : %d\r\n", HAL_GetTick());
			cnt = 0;
		}
	}
}

void normalboot(void)
{
	// Normal boot clear mailbox and jump to application
	leds_0_off();
	leds_1_off();
	iapMailbox[0] = 0;
	iapMailbox[1] = 0;
	dbg_printf("NB\r\n");
	iap_JumpToApplication();
}

void Board_SystickCallBack()
{
	static uint32_t m;
	if (bl)
	{
		if ((m % 250)==0)
		{
			leds_1_flash(100);
			m = 0;
		}
		m++;
	}
}

